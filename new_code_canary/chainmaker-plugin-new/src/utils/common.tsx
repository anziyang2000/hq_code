function ConfirmModalComponent(
  {
    title,
    children,
    ...props
  }: {
    title: string;
    children: React.ReactNode;
    confirmBtnText?: string;
    cancelBtnText?: string;
    isHideConfirmBtn?: boolean;
  },
  ref,
) {
  const [visible, setVisible] = useState(false);
  const [loading, setLoading] = useState<boolean>();
  const [conf, setConf] = useState<{
    close?: () => void;
    confirm?: () => Promise<any>;
  }>({});
  useImperativeHandle(ref, () => ({
    show: (p: { close?: () => void; confirm?: () => Promise<any> }) => {
      setConf(p);
      setVisible(true);
    },
  }));

  const onConfirm = useCallback(() => {
    setLoading(true);
    if (conf.confirm) {
      const confirmPromise = conf.confirm();
      // @ts-ignore
      confirmPromise
        ?.then((res) => {
          setVisible(false);
        })
        .finally(() => {
          setLoading(false);
        });
    } else {
      setLoading(false);
      setVisible(false);
    }
  }, [conf]);

  const onCancel = useCallback(() => {
    conf?.close?.();
    setVisible(false);
  }, [conf]);

  return (
    <Modal className="confirm-model" visible={visible} caption={title} onClose={onCancel} disableCloseIcon>
      <Modal.Body>{children} </Modal.Body>
      <Modal.Footer>
        <Button type="weak" onClick={onCancel}>
          {props.cancelBtnText || 'ÊàëÂÜçÊÉ≥ÊÉ≥'}
        </Button>
        {!props.isHideConfirmBtn && (
          <Button type="primary" onClick={onConfirm} loading={loading}>
            {props.confirmBtnText || 'Á°ÆÂÆö'}
          </Button>
        )}
      </Modal.Footer>
    </Modal>
  );
}

export const ConfirmModal = React.forwardRef(ConfirmModalComponent);

